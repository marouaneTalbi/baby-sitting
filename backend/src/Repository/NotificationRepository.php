class NotificationRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Notification::class);
    }


    public function findUserNotifications(int $id): array
    {
        return $this->createQueryBuilder('n')
        ->leftJoin('n.parent', 'p') // Jointure avec l'entité User (parent)
        ->leftJoin('p.profile', 'pr') // Jointure avec l'entité Profile du parent
        ->addSelect('p', 'pr') // Sélection des données du parent et de son profile
        ->where('n.parent = :id OR n.serviceProvider = :id')
        ->setParameter('id', $id)
        ->getQuery()
        ->getResult();
    }

